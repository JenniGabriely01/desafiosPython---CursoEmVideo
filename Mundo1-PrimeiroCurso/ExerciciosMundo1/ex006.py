# Crie um algoritimo que leia um número e mostre o seu dobro, o seu triplo e a raiz quadrada
n1 = int(input("Digite um número:"))
dobro = n1 * 2
triplo = n1 * 3
rQuadrada = n1 ** (1/2) 

print(" O dobro do número é: {} \nO triplo do número é: {} \nA raiz quadrada do número é: {}".format(dobro, triplo, rQuadrada))